#ifndef	_MEM_MT6205_H_
#define	_MEM_MT6205_H_

// MT6205 External Memory Interface Control Parameter

// Burst Read Mode Control 
#define	MT6205_BREN_DISABLE		0	// Disable
#define	MT6205_BREN_ENABLE		1	// Enable

// Burst Read Latency 
#define	MT6205_BRLT_ZERO		0	// Zero wait state
#define	MT6205_BRLT_ONE			1	// One wait state

// Burst Read Length Control 
#define	MT6205_BRL_4WORDS		0	// 4 continuous words, 4x4 bytes of page size
#define	MT6205_BRL_8WORDS		1	// 8 continuous words, 8x4 bytes of page size

// Read Byte Lane Enable 
#define	MT6205_RBLN_HELDLOW		1	// All bytes lanes held low during system reads
#define	MT6205_RBLN_HELDHIGH	0	// All bytes lanes held high during system reads

// Data Width 
#define	MT6205_DWIDTH_16BIT		0	// 16-bits device
#define	MT6205_DWIDTH_8BIT		1	// 8-bits device

#endif
